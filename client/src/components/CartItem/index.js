import React from 'react';
// imports UseDispatch hook from react-redux
import { useDispatch } from 'react-redux';
import { REMOVE_FROM_CART, UPDATE_CART_QUANTITY } from "../../utils/actions";
import { idbPromise } from "../../utils/helpers";

const CartItem = ({ item }) => {
  // sets useDispatch hook to a variable
  const dispatch = useDispatch();

  const removeFromCart = item => {
    // reducer to remove item from cart
    dispatch({
      type: REMOVE_FROM_CART,
      _id: item._id
    });
    idbPromise('cart', 'delete', { ...item });

  };

  const onChange = (e) => {
    const value = e.target.value;
    if (value === '0') {
      // reducer to remove item from cart
      dispatch({
        type: REMOVE_FROM_CART,
        _id: item._id
      });
      idbPromise('cart', 'delete', { ...item });

    } else {
      // reducer to update item wuantity in a cart
      dispatch({
        type: UPDATE_CART_QUANTITY,
        _id: item._id,
        purchaseQuantity: parseInt(value)
      });
      idbPromise('cart', 'put', { ...item, purchaseQuantity: parseInt(value) });

    }
  }

  return (
    <div className="flex-row">
      <div>
        <img
          src={`/images/${item.image}`}
          alt=""
        />
      </div>
      <div>
        <div>{item.name}, ${item.price}</div>
        <div>
          <span>Qty:</span>
          <input
            type="number"
            placeholder="1"
            value={item.purchaseQuantity}
            // function that runs either remove or update reducer based on user input in input field
            onChange={onChange}
          />
          <span
            role="img"
            aria-label="trash"
            // runs remove reducer on icon click
            onClick={() => removeFromCart(item)}
          >
            üóëÔ∏è
          </span>
        </div>
      </div>
    </div>
  );
}

export default CartItem;